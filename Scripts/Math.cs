using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

class Math
{
    public static float cubed(float n)
    {
        float x = n * n * n;
        return x;
    }

    public static float squared(float n)
    {
        float x = n * n;
        return x;
    }

}
